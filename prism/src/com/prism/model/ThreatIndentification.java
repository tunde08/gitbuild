package com.prism.model;

/**
 * @author Tunde
 *
 */
public class ThreatIndentification {
	
	private int threatId;
	private String threatName;
	private String threatType;
	private String threatSource;
	private String threatLocation;
	/**
	 * @return the threatId
	 */
	public int getThreatId() {
		return threatId;
	}
	/**
	 * @param threatId the threatId to set
	 */
	public void setThreatId(int threatId) {
		this.threatId = threatId;
	}
	/**
	 * @return the threatName
	 */
	public String getThreatName() {
		return threatName;
	}
	/**
	 * @param threatName the threatName to set
	 */
	public void setThreatName(String threatName) {
		this.threatName = threatName;
	}
	/**
	 * @return the threatType
	 */
	public String getThreatType() {
		return threatType;
	}
	/**
	 * @param threatType the threatType to set
	 */
	public void setThreatType(String threatType) {
		this.threatType = threatType;
	}
	/**
	 * @return the threatSource
	 */
	public String getThreatSource() {
		return threatSource;
	}
	/**
	 * @param threatSource the threatSource to set
	 */
	public void setThreatSource(String threatSource) {
		this.threatSource = threatSource;
	}
	/**
	 * @return the threatLocation
	 */
	public String getThreatLocation() {
		return threatLocation;
	}
	/**
	 * @param threatLocation the threatLocation to set
	 */
	public void setThreatLocation(String threatLocation) {
		this.threatLocation = threatLocation;
	}

}
